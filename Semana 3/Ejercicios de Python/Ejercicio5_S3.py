'''Cree un algoritmo que defina una cantidad de metros (por ejemplo, 5) y luego use print() para mostrar cuántos centímetros son
Ejemplo:
Salida:
"5 metros son 500 centímetros"'''

# Definir una cantidad de metros
metros = 5

# Calcular cuántos centímetros son (1 metro = 100 centímetros)
centimetros = metros * 100

# Mostrar el resultado
print(metros, "metros son", centimetros, "centímetros")
