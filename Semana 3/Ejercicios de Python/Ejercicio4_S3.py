'''Cree un algoritmo que muestre cuántos años tendrá usted dentro de 10 años. El algoritmo debe usar print() y una operación de suma'''
# Pedir la edad actual al usuario
edad_actual = int(input("¿Cuántos años tienes actualmente? "))

# Calcular la edad dentro de 10 años
edad_futura = edad_actual + 10

# Mostrar el resultado
print("Dentro de 10 años tendrás", edad_futura, "años.")
